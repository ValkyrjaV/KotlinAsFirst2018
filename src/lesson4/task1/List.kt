 * Множители в результирующей строке должны располагаться по возрастанию.
 */
fun factorizeToString(n: Int): String = factorize(n).joinToString(separator = "*")

/**
 * Средняя
 *
 * Перевести заданное целое число n >= 0 в систему счисления с основанием base > 1.
 * Результат перевода вернуть в виде списка цифр в base-ичной системе от старшей к младшей,
 * например: n = 100, base = 4 -> (1, 2, 1, 0) или n = 250, base = 14 -> (1, 3, 12)
 */
fun convert(n: Int, base: Int): List<Int> {
    var copyn = n
    val new = mutableListOf<Int>()
    if (n == 0) return listOf(0)
    while (copyn > 0) {
        new.add(copyn % base)
        copyn /= base
    }
    return new.reversed()
}

/**
 * Сложная
 *
 * Перевести заданное целое число n >= 0 в систему счисления с основанием 1 < base < 37.
 * Результат перевода вернуть в виде строки, цифры более 9 представлять латинскими
 * строчными буквами: 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: n = 100, base = 4 -> 1210, n = 250, base = 14 -> 13c
 */
fun convertToString(n: Int, base: Int): String {
    val const = ("abcdefghijklmnopqrstuvwxyz")
    val before = convert(n, base)
    val new = mutableListOf(String())
    for (i in 0 until before.size) {
        if (before[i] < 10)
            new.add(before[i].toString())
        else {
            new.add((const[before[i] - 10]).toString())
        }
    }
    return new.joinToString(separator = "")
}

/**
 * Средняя
 *
 * Перевести число, представленное списком цифр digits от старшей к младшей,
 * из системы счисления с основанием base в десятичную.
 * Например: digits = (1, 3, 12), base = 14 -> 250
 */
fun decimal(digits: List<Int>, base: Int): Int {
    var result = 0.0
    for (i in 0 until digits.size) {
        result += digits[i] * (base.toDouble().pow(digits.size - i - 1))
    }
    return result.toInt()
}

/**
 * Сложная
 *
 * Перевести число, представленное цифровой строкой str,
 * из системы счисления с основанием base в десятичную.
 * Цифры более 9 представляются латинскими строчными буквами:
 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 */
fun decimalFromString(str: String, base: Int): Int {
    val list = mutableListOf<Int>()
    val const = "abcdefghijklmnopqrstuvwxyz"
    for (char in str) {
        if (char in const) {
            list.add((const.indexOf(char, 0)) + 10)
        } else list.add(char.toInt() - '0'.toInt())
    }
    return decimal(list, base)
}

/**
 * Сложная
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    val thou = n / 1000
    val number = mutableListOf<String>()
    if (thou != 0)
        for (i in 1..thou) {
            number.add("M")
        }
    val rest1 = n % 1000 / 100
    val rest2 = n % 100 / 10
    val rest3 = n % 10
    when {
        rest1 == 9 -> number.add("CM")
        rest1 >= 5 -> {
            number.add("D")
            for (i in 1..(rest1 - 5)) {
                number.add("C")
            }
        }
        rest1 == 4 -> number.add("CD")
        rest1 >= 1 -> {
            for (i in 1..rest1) {
                number.add("C")
            }
        }
    }
    when {
        rest2 == 9 -> number.add("XC")
        rest2 >= 5 -> {
            number.add("L")
            for (i in 1..(rest2 - 5)) {
                number.add("X")
            }
        }
        rest2 == 4 -> number.add("XL")
        rest2 >= 1 -> {
            for (i in 1..rest2) {
                number.add("X")
            }
        }
    }
    when {
        rest3 == 9 -> number.add("IX")
        rest3 >= 5 -> {
            number.add("V")
            for (i in 1..(rest3 - 5)) {
                number.add("I")
            }
        }
        rest3 == 4 -> number.add("IV")
        rest3 >= 1 -> {
            for (i in 1..rest3) {
                number.add("I")
            }
        }
    }
    return number.joinToString(separator = "")
}

